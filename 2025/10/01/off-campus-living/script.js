function handleStepEnter(response) {
  step.classed("is-active", function (d, i) {
    return i === response.index;
  });

  const stepIndex = parseInt(response.element.dataset.step);

  const video = document.getElementById("bg-video");
  const mapContainer = document.querySelector(".mapboxgl-map");

  if (stepIndex <= 3) {
    video.style.opacity = "1";
    if (mapContainer) mapContainer.style.opacity = "0";
  } else {
    video.style.opacity = "0";
    if (mapContainer) mapContainer.style.opacity = "1";
  }

  if (stepIndex > 3) {
    const coords = response.element
      .getAttribute("data-coords")
      .split(",")
      .map(Number);
    if (coords[0] == 0 && coords[1] == 0) {
      map.flyTo({ center: startCoords, zoom: 14.5 });
    } else {
      map.flyTo({ center: coords, zoom: 15.6 });
    }
  }
}

